package non_weighted_graphs.directed_graphs.DFS;

import non_weighted_graphs.undirected_graphs.Graph;
import non_weighted_graphs.undirected_graphs.Paths;

import java.util.Stack;

public class DirectedDFS implements Paths {
    private boolean[] marked;
    private int[] edgeTo;
    private int s;

    public DirectedDFS(Graph G, int s) {
        this.s = s;
        marked = new boolean[G.V()];
        edgeTo = new int[G.V()];
        dfs(G, s);
    }

    private void dfs(Graph G, int v) {
        marked[v] = true;
        for (int w : G.adj(v)) {
            if (!marked[w]) {
                dfs(G, w);
                edgeTo[w] = v;
            }
        }
    }

    @Override
    public boolean hasPathTo(int v) {
        return marked[v];
    }

    public Iterable<Integer> pathTo(int v) {
        if (!hasPathTo(v)) return null;

        Stack<Integer> path = new Stack<>();
        for (int x = v; x != s; x = edgeTo[x]) {
            path.push(x);
        }
        path.push(s);
        return path;
    }
}
